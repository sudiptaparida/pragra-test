package JUNE_JAVA;

public class Swap {
    public static void main(String[] args)
    {
        int x=10;
        int y =20;
        int swap;
        swap=x;
        x=y;
        y=swap;
        System.out.println("the value of x is :" +x);
        System.out.println(" the value of y is :" +y);


    }
}
